class car
{
	private String numplate,model,color;

	public void set_numplatecar(String x)
	{
		this.numplate=x;
		
	}

	public String get_numplatecar()
	{
		return numplate;
	}

	public void set_modelcar(String x)
	{
		this.model=x;
		
	}

	public String get_modelcar()
	{
		return model;
	}

	public void set_colorcar(String x)
	{
		this.color=x;
		
	}

	public String get_colorcar()
	{
		return color;
	}

	
}