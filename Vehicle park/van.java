class van
{
	private String numplate,model,color,seats;

	public void set_numplatevan(String x)
	{
		this.numplate=x;
		
	}

	public String get_numplatevan()
	{
		return numplate;
	}

	public void set_modelvan(String x)
	{
		this.model=x;
		
	}

	public String get_modelvan()
	{
		return model;
	}

	public void set_colorvan(String x)
	{
		this.color=x;
		
	}

	public String get_colorvan()
	{
		return color;
	}

	public void set_seatsvan(String x)
	{
		this.seats=x;
		
	}

	public String get_seatsvan()
	{
		return seats;
	}


}