class wheel
{
	private String numplate,model,color;

	public void set_numplatewheel(String x)
	{
		this.numplate=x;
		
	}

	public String get_numplatewheel()
	{
		return numplate;
	}

	public void set_modelwheel(String x)
	{
		this.model=x;
		
	}

	public String get_modelwheel()
	{
		return model;
	}

	public void set_colorwheel(String x)
	{
		this.color=x;
		
	}

	public String get_colorwheel()
	{
		return color;
	}
}